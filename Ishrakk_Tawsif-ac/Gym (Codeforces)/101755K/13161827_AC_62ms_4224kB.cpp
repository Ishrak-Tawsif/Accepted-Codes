int n,need,ar[200005];

bool check(int val)
{
    int nici = 0,con=0;
    for(int i=1; i<=n; i++)
    {
        if(ar[i]>nici) {if(con<val)con ++,nici++;}
        else nici++;//if(val == 1) cout<<ar[i]<<" "<<nici<<" "<<con<<endl;
    }//if(val == 1) out(nici);
    return nici>=need;
}

int main()
{
    scanf("%d %d", &n,&need);

    for(int i=1; i<=n; i++) sf(ar[i]);

    int st = 0, en = need,res;

    while(st<=en)
    {
        int mid = (st + en)>>1;
        if(check(mid)) res = mid,en = mid - 1;
        else st = mid + 1;
    }
    pf("%d\n", res);

    return 0;
}