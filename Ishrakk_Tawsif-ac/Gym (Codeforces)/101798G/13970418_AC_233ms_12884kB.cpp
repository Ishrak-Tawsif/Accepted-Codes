ll ar[mx];

int main()
{
    int n;

    sf(n);

    set<pair<ll,int> > st,sat;

    for(int i=0; i<n; i++) sfll(ar[i]);

    sort(ar, ar+n);

    for(int i=0; i<n; i++) st.insert(mp(i,ar[i]));

    ll ans = 0;
    while(st.size()>1)
    {
        while(!st.empty())
        {
            auto temp = * st.begin();
            st.erase(st.begin());

            auto temp2 = * --st.end();
            st.erase(--st.end());

            ans += abs(temp.second - temp2.second);

            if(temp.second>temp2.second) sat.insert(temp);
            else sat.insert(temp2);
        }
        st = sat;
        while(!sat.empty()) sat.erase(sat.begin());
    }

    out(ans);

    return 0;
}